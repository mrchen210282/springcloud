package cn.bitflash.entity;

public class UserVipConditionsEntity {

    private int level;

    private float power;

    private int bkcount;

    private int state;

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public float getPower() {
        return power;
    }

    public void setPower(float power) {
        this.power = power;
    }

    public int getBkcount() {
        return bkcount;
    }

    public void setBkcount(int bkcount) {
        this.bkcount = bkcount;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}
